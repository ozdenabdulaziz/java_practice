package day04;

public class Variables_Practice {

    public static void main(String[] args) {

        byte byt1 = -128;
        byte byt2 = 127;

        System.out.print(byt1 + ", ");
        System.out.print(byt2);

        System.out.println("===========================");

        short shrt1 = -32768;
        short shrt2 = 32767;

        System.out.print(shrt1 + ", ");
        System.out.print(shrt2);

        System.out.println("===========================");

        int int1 = -2147483648;
        int int2 = 2147483647;

        System.out.print(int1 + ", ");
        System.out.println(int2);

        System.out.println("===========================");




    }


}
